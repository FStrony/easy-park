package br.com.easypark.to;

import java.math.BigDecimal;

/**
 * Esta classe tem por objetivo inserir e retornar os valores do tipo de veiculo que pode ser estacionado no estacionamento
 * 
 * @author ExceptionIT
 * @since 20/04/2013
 * @version 1.0
 */

public class TipoVeiculoTO {

	private long id;

	private String descricao;

	private BigDecimal valor;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public BigDecimal getValor() {
		return valor;
	}

	public void setValor(BigDecimal valor) {
		this.valor = valor;
	}

}
